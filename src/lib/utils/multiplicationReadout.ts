export function getMultiplicationReadout(row: number, column: number): string {
  if (!Number.isInteger(row) || row < 1 || row > 10 || !Number.isInteger(column) || column < 1 || column > 9) {
    throw new RangeError(`Unsupported multiplication cell: ${row} x ${column}`)
  }

  return READOUTS[row - 1][column - 1]
}
